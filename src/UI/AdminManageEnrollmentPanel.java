    private void initTable() {
        tableModel = new DefaultTableModel(
                new Object[]{"Student ID", "First Name", "Last Name", "Status"}, 0
        );
        tblStudents.setModel(tableModel);
        tblStudents.setSelectionMode(ListSelectionModel.MULTIPLE_INTERVAL_SELECTION);
    }

    private void loadAcademicYears() throws SQLException {
        years = academicYearDAO.getAllAcademicYears();
        cboAcademicYear.removeAllItems();
        for (AcademicYear y : years) {
            String label = y.getYearLabel() + (y.getStatus().equalsIgnoreCase("OPEN") ? " (ACTIVE)" : "");
            cboAcademicYear.addItem(label);
        }
        cboAcademicYear.addActionListener(e -> updateStatusBasedOnSelection());
    }

    private void updateStatusBasedOnSelection() {
        int selectedIndex = cboAcademicYear.getSelectedIndex();
        if (selectedIndex != -1 && years != null && !years.isEmpty()) {
            AcademicYear selectedYear = years.get(selectedIndex);
            if (selectedYear.getStatus().equalsIgnoreCase("OPEN")) {
                txtStatus.setText("ACTIVE");
                txtStatus.setForeground(Color.GREEN);
            } else {
                txtStatus.setText("INACTIVE");
                txtStatus.setForeground(Color.RED);
            }
        }
    }

    private void loadGradeLevels() throws SQLException {
        cbGradeLevel.removeAllItems();
        GradeLevel placeholderGrade = new GradeLevel();
        placeholderGrade.setGradeLevelId(-1);
        placeholderGrade.setGradeLevelName("Select");
        cbGradeLevel.addItem(placeholderGrade);
        for (GradeLevel gl : gradeLevelDAO.getAllGradeLevels()) {
            cbGradeLevel.addItem(gl);
        }
    }
    
    private void loadSections() throws SQLException {
        cbSection.removeAllItems();
        Section placeholderSection = new Section(-1, "Select");
        cbSection.addItem(placeholderSection);
        for (Object[] sec : sectionDAO.getAllSections()) {
            int sectionId = (int) sec[0];
            String sectionName = (String) sec[2];
            Section section = new Section(sectionId, sectionName);
            cbSection.addItem(section);
        }
    }
    
    private void addGradeLevelSectionSync() {
        cbGradeLevel.addActionListener(e -> {
            if (isUpdatingGrade) return;
            try {
                isUpdatingSection = true;
                
                GradeLevel selectedGrade = (GradeLevel) cbGradeLevel.getSelectedItem();
                cbSection.removeAllItems();

                if (selectedGrade != null) {
                    
                    List<Object[]> sections = sectionDAO.getSectionsByGrade(selectedGrade.getGradeLevelName());
                    for (Object[] sec : sections) {
                        int sectionId = (int) sec[0];
                        String sectionName = (String) sec[2];
                        Section section = new Section(sectionId, sectionName);
                        cbSection.addItem(section);
                    }
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error loading sections: " + ex.getMessage());
            } finally {
                isUpdatingSection = false;
            }
        });
        
        cbSection.addActionListener(e -> {
            if (isUpdatingSection) return;
            try {
                isUpdatingGrade = true;
                
                Section selectedSection = (Section) cbSection.getSelectedItem();
                if (selectedSection != null) {
                    String gradeName = sectionDAO.getGradeBySection(selectedSection.getSectionName());
                    if (gradeName != null) {
                        for (int i = 0; i < cbGradeLevel.getItemCount(); i++) {
                            GradeLevel gl = cbGradeLevel.getItemAt(i);
                            if (gl.getGradeLevelName().equals(gradeName)) {
                                cbGradeLevel.setSelectedItem(gl);
                                break;
                            }
                        }
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error loading grade level: " + ex.getMessage());
            } finally {
                isUpdatingGrade = false;
            }
        });
    }

    private void loadActiveYearStatus() {
        try {
            AcademicYear activeYear = academicYearDAO.getActiveYear();
            if (activeYear != null) {
                txtStatus.setText("ACTIVE (" + activeYear.getYearLabel() + ")");
                txtStatus.setForeground(new Color(0, 153, 0));
            } else {
                txtStatus.setText("INACTIVE");
                txtStatus.setForeground(Color.RED);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            txtStatus.setText("Error");
            txtStatus.setForeground(Color.RED);
        }
    }

    private void getAllStudents() throws SQLException {
        tableModel.setRowCount(0);
        List<Student> students = studentDAO.getAllStudentsWithStatus(activeYearId);
        for (Student s : students) {
            tableModel.addRow(new Object[]{
                s.getStudentId(),
                s.getFirstName(),
                s.getLastName(),
                s.getCurrentStatus()
            });
        }
    }

    private void addLogic() {
        btnRefresh.addActionListener(e -> {
            try {
                getAllStudents();
            } catch (SQLException ex) {
                Logger.getLogger(AdminManageEnrollmentPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        });

        tblStudents.getSelectionModel().addListSelectionListener(e -> {
            int row = tblStudents.getSelectedRow();
            if (row != -1) {
                int studentId = (int) tableModel.getValueAt(row, 0);
                String name = tableModel.getValueAt(row, 1) + " " + tableModel.getValueAt(row, 2);

                txtStudentId.setText(String.valueOf(studentId));
                txtName.setText(name);

                try {
                    String status = enrollmentDAO.getStudentEnrollmentStatus(studentId, activeYearId);
                    String section = enrollmentDAO.getStudentSection(studentId, activeYearId);
                    String gradeLevel = enrollmentDAO.getStudentGradeLevel(studentId, activeYearId);

                    txtEnrollmentStatus.setText(status);
                    txtSection.setText(section);
                    txtGradeLevel.setText(gradeLevel);
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error loading enrollment info: " + ex.getMessage());
                }
            }
        });

        btnEnrollSelected.addActionListener(e -> {
            int[] selectedRows = tblStudents.getSelectedRows();
            if (selectedRows.length == 0) {
                JOptionPane.showMessageDialog(this, "Please select at least one student to enroll.");
                return;
            }

            List<Integer> selectedStudentIds = new ArrayList<>();
            for (int row : selectedRows) {
                int studentId = Integer.parseInt(tblStudents.getValueAt(row, 0).toString());
                selectedStudentIds.add(studentId);
            }
            
            GradeLevel selectedGradeLevel = (GradeLevel) cbGradeLevel.getSelectedItem();
            Section selectedSection = (Section) cbSection.getSelectedItem();

            if (selectedGradeLevel == null || selectedSection == null ||
                "Select".equals(selectedGradeLevel.toString()) ||
                "Select".equals(selectedSection.toString())) {
                JOptionPane.showMessageDialog(this, "Please select a grade level and section.");
                return;
            }

            int gradeLevelId = selectedGradeLevel.getGradeLevelId();
            int sectionId = selectedSection.getSectionId();

            // Get active academic year
            AcademicYear activeYear = null;
            try {
                activeYear = academicYearDAO.getActiveYear();
                if (activeYear == null) {
                    JOptionPane.showMessageDialog(this, "No active academic year found. Please open an academic year first.");
                    return;
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error fetching active academic year.");
                return;
            }

            // Confirm enrollment
            int confirm = JOptionPane.showConfirmDialog(this,
                "Enroll " + selectedStudentIds.size() + " selected students to section " + selectedSection.toString() + "?",
                "Confirm Enrollment",
                JOptionPane.YES_NO_OPTION
            );
            if (confirm != JOptionPane.YES_OPTION) return;

            try {
                EnrollmentResult result = enrollmentDAO.enrollMultipleStudents(
                    selectedStudentIds, gradeLevelId, sectionId, activeYear.getYearId()
                );

                String message = result.getEnrolledCount() + " student(s) enrolled successfully.";
                if (!result.getSkippedStudents().isEmpty()) {
                    message += "\n\nSkipped (already enrolled):\n" + String.join(", ", result.getSkippedStudents());
                }

                JOptionPane.showMessageDialog(this, message);
                getAllStudents();

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Enrollment failed: " + ex.getMessage());
            }
        });

        btnUnenrollSelected.addActionListener(e -> {
            int[] selectedRows = tblStudents.getSelectedRows();
            if (selectedRows.length == 0) {
                JOptionPane.showMessageDialog(this, "Please select at least one student to unenroll.");
                return;
            }

            List<Integer> selectedStudentIds = new ArrayList<>();
            for (int row : selectedRows) {
                int studentId = Integer.parseInt(tblStudents.getValueAt(row, 0).toString());
                selectedStudentIds.add(studentId);
            }

            AcademicYear activeYear = null;
            try {
                activeYear = academicYearDAO.getActiveYear();
                if (activeYear == null) {
                    JOptionPane.showMessageDialog(this, "No active academic year found. Please open an academic year first.");
                    return;
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error fetching active academic year.");
                return;
            }

            int confirm = JOptionPane.showConfirmDialog(
                this,
                "Unenroll " + selectedStudentIds.size() + " selected student(s) for " + activeYear.getYearLabel() + "?",
                "Confirm Unenrollment",
                JOptionPane.YES_NO_OPTION
            );

            if (confirm != JOptionPane.YES_OPTION) return;

            try {
                Map<String, String> results = enrollmentDAO.unenrollMultipleStudentsDetailed(
                    selectedStudentIds, activeYear.getYearId()
                );

                StringBuilder message = new StringBuilder("<html><body>");
                for (Map.Entry<String, String> entry : results.entrySet()) {
                    message.append("â€¢ <b>").append(entry.getKey()).append("</b>: ")
                           .append(entry.getValue()).append("<br>");
                }
                message.append("</body></html>");

                JOptionPane.showMessageDialog(this, new JLabel(message.toString()), 
                                              "Unenrollment Summary", JOptionPane.INFORMATION_MESSAGE);

                getAllStudents(); // refresh table

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Unenrollment failed: " + ex.getMessage());
            }
        });

        btnSetActiveYear.addActionListener(e -> {
            try {
                int selectedIndex = cboAcademicYear.getSelectedIndex();
                if (selectedIndex == -1) {
                    JOptionPane.showMessageDialog(this, "Please select an academic year first.");
                    return;
                }

                AcademicYear selectedYear = years.get(selectedIndex);

                int confirm = JOptionPane.showConfirmDialog(
                        this,
                        "Set " + selectedYear.getYearLabel() + " as ACTIVE?",
                        "Confirm",
                        JOptionPane.YES_NO_OPTION
                );

                if (confirm == JOptionPane.YES_OPTION) {
                    academicYearDAO.setActiveYear(selectedYear.getYearId());
                    JOptionPane.showMessageDialog(this,
                            "Academic year " + selectedYear.getYearLabel() + " is now ACTIVE.");
                    loadAcademicYears();
                    loadActiveYear();
                    updateStatusBasedOnSelection();
                    getAllStudents();
                }

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this,
                        "Error updating active year: " + ex.getMessage(),
                        "Database Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        });
        
        btnEndAY.addActionListener(e -> {
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "End the current academic year?\nAll students will be set to 'Not Enrolled' and year will be CLOSED.",
                    "Confirm",
                    JOptionPane.YES_NO_OPTION
            );

            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    enrollmentDAO.markEnrollmentsInactiveForYear(activeYearId);
                    academicYearDAO.endActiveAcademicYear();
                    loadAcademicYears();
                    loadActiveYear();
                    getAllStudents();

                    txtStatus.setText("CLOSED");
                    txtStatus.setForeground(Color.RED);

                    JOptionPane.showMessageDialog(this, "Academic year successfully ended.");
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error ending year: " + ex.getMessage());
                }
            }
        });
    }

    private void loadActiveYear() throws SQLException {
        AcademicYear active = academicYearDAO.getActiveYear();
        if (active != null) {
            activeYearId = active.getYearId();
            cboAcademicYear.setSelectedItem(active.getYearLabel());
        }
    }

